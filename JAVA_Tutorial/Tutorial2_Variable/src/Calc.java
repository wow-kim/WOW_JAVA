
public class Calc {

	public static void main(String[] args) {
		
		int a = 1;
		int b = 2;
		
		System.out.println("a + b = " + (a + b)); // 문자열 + 정수형?, 신기하군
		System.out.println("a - b = " + (a - b));
		System.out.println("a * b = " + (a * b));
		System.out.println("a / b = " + (a / b)); // 몫
		System.out.println("a % b = " + (a % b)); // 나머지
		
	}

}
